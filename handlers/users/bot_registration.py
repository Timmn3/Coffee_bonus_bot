    number = message.text
    if number == "–û—Ç–º–µ–Ω–∏—Ç—å —Ä–µ–≥–∏—Å—Ç—Ä–∞—Ü–∏—é":
        await state.finish()
        await message.answer('–û—Ç–º–µ–Ω–µ–Ω–æ', reply_markup=kb_main)
    else:
        user_id = int(message.from_user.id)

